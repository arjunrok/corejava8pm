package polymorphism;

public class Polymorphism {
	/*
	 *   ================ Polymorphism ==============
	 *   # Mulitple-form
	 *   # same methods but different outputs
	 *   # to achieve polymorphism we have to use:
	 *   		a> method overloading
	 *   		b> method overriding
	 *   
	 *   e.g.
	 *   
	 *       //   to calculate area 
	 *       
	 *            // printArea():
	 *          
	 *           //1. area for rectangle
	 *           //2. area for circle
	 *           //3. area for square
	 *           //4. area for triangle
	 *           
	 *          //to implement  deposit():
	 *          //1. deposit for cash
	 *         //2. deposit for cheque
	 *         //3. deposit for online
	 *         
	 *         //====================== Method overloading ==================
	 *         # use to achieve polymorphism in java
	 *         
	 *         # we can create multiple method of same name,same return type
	 *           but different list of arguments in same class is known as 
	 *            method overloading.
	 *            
	 *         # also known as compile time polymorphism 
	 *         
	 *         # if number arguments are same then we can change data type as well.
	 *   
	 */

}
