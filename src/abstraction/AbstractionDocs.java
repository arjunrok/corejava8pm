package abstraction;

public class AbstractionDocs {
	/*
	 * ============================== Abstraction ========================================
	 * 
	 *  # use to hide implementation details in the program .
	 *  
	 *  # To achieve abstraction :
	 *  
	 *    a> Abstract class :
	 *    
	 *       # class which contains abstract(unimplemented methods) and non-abstract(implemented methods) methods
	 *         is known as abstract class.
	 *         
	 *       		# abstract method :
	 *       			@ method which does not have implementation details or body.
	 *                 e.g.
	 *                   abstract void print();
	 *                   abstract int sum(int a, int b);
	 *       
	 *        #  we can't create object of abstract class.
	 *        
	 *        #  every abstract must have at least one child class and child class must override
	 *           abstract methods. 
	 *           
	 *        # it a level of abstraction (0 to 100 %)   
	 *        
	 *             syntax :
	 *             
	 *           abstract  class  class_name{
	 *             
	 *                //abstract methods 
	 *                e.g.
	 *                   abstract void sms();
	 *                   abstract int getArea();
	 *                
	 *                //non-abstract methods
	 *                e.g.
	 *                void print(){
	 *                
	 *                   }
	 *             }
	 *         
	 *         
	 *    b> Interface :
	 *    
	 *      # same as class which contains public abstract methods and public static final variables only.
	 *      # we can't create object of interface.
	 *      # every interface must have at least one implementation class and impl. class must 
	 *         override abstract methods.
	 *         
	 *      # its 100% abstraction
	 *      
	 *      syntax :
	 *      
	 *      interface interface_name {
	 *      
	 *             //public static final variables
	 *             
	 *            //public abstract methods
	 *      }
	 *      
	 */

}




