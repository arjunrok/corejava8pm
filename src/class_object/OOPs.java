package class_object;

public class OOPs {
	/*
	 *  ================= OOPS(Object Oriented Programming) ===========
	 *  
	 *  1> Class & Object
	 *  2> Encapsulation
	 *  3> Inheritance
	 *  4> Polymorphism
	 *  5> Abstraction
	 *    a> Abstract class
	 *    b> Interface
	 *    
	 *    //=========================== class ============================================
	 *    # use to represent real world entity (things. it may be living,non-living or conceptual things)
	 *    # it also called reference data type.
	 *    # class is blue print or template of object.
	 *    # collection/group of object having similar behaviors and features.
	 *    # class contains properties(data) and methods(function).
	 *    
	 *    syntax :
	 *    
	 *      class  class-name {
	 *      
	 *             //properties/features/attributes/data
	 *             
	 *             //methods/function/behaviors/responsibilities
	 *      }
	 *      
	 *    //=============================== object/instance =============================
	 *    # implementation of class is known as object.
	 *    # instance of class.
	 *    
	 *    
	 *    Syntax :
	 *      class_name   obj_name =  new class_name();
	 *      
  	 *    e.g.
  	 *    
  	 *    //class-A
  	 *    
  	 *       class A{
  	 *       
  	 *       
  	 *       }
  	 *       
  	 *    //objects
  	 *    e.g.
  	 *    
  	 *      A  a = new A();
  	 *      
  	 *     Scanner sc = new Scanner();
  	 *     
  	 *     JFrame jf = new JFrame();   
	 *      
	 */

}
