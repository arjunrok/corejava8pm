package constructor;

public class ConstructorDocs {
	
	/*
	 *     =====================  Constructor ================================
	 *     # use to create object in java.
	 *     # special type of method which has same name as class name
	 *       is called constructor.
	 *     # constructor does not have return type .  
	 *     # if there is no any user defined constructor in class, then 
	 *       default constructor is called implicitly.
	 *       
	 *       
	 *   syntax :
	 *   
	 *    class_name(){
	 *    
	 *       }
	 *       
	 *   # Types of constructor :
	 *   
	 *       1> default constructor :
	 *          # use to create object with its default values.
	 *          
	 *          syntax :
	 *          
	 *          class_name(){
	 *          
	 *                //set default values to object
	 *          }
	 *          
	 *       2> parameterized constructor:
	 *       	# use to create object with its dynamic values.
	 *       
	 *      syntax :
	 *       class_name(arg1,arg2,....,argn){
	 *         
	 *            //set dynamic values to object
	 *       }
	 */

}
